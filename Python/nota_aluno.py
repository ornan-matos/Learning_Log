#A expressão será utilizada para decidir se um aluno foi ou não aprovado.
#Para ser aprovado a media geral precisa ser maior que 7.
matematica = 7 
biologia = 8
portugues = 10
fisica = 4

media = (matematica + biologia + portugues + fisica) //4 

print ("Resultado Final: ", media)
print ("Aluno Aprovado: ", media >= 7)

